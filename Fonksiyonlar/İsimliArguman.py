#Python fonksiyonlarında öğe sıralamasının önem taşıdığı argümanlara “sıralı argüman positional argument adı verilir

def islem(a ,b ,c):
    print(a/b)
    print(c)
islem(8,4,"merhaba")

#Böylece argümanları sıra gözetmeden kullanma imkânımız oluyor

def islem(a ,b ,c):
    print(a/b)
    print(c)
islem(a=8,c="merhaba",b=4)


