#Bir listeden elemanı kendisini belirterek çıkarmak için remove()

Liste=["İstanbul","ankara","izmir","bursa"]
Liste.remove("İstanbul") #istanbulu cıkardık
print(Liste)


#Bir listeden in belirterek çıkarmak için remove()

Liste=["İstanbul","ankara","izmir","bursa"]
Liste.pop(1) #ankarayı cıkardık
print(Liste)
